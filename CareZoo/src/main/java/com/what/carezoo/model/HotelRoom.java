package com.what.carezoo.model;

public class HotelRoom {
	private int h_num;
	private int r_num;
	private int r_p_count;
	private int r_adult;
	private int r_child;
	private int r_count;
	private String r_fileName;
	public int getH_num() {
		return h_num;
	}
	public void setH_num(int h_num) {
		this.h_num = h_num;
	}
	public int getR_num() {
		return r_num;
	}
	public void setR_num(int r_num) {
		this.r_num = r_num;
	}
	public int getR_p_count() {
		return r_p_count;
	}
	public void setR_p_count(int r_p_count) {
		this.r_p_count = r_p_count;
	}
	public int getR_adult() {
		return r_adult;
	}
	public void setR_adult(int r_adult) {
		this.r_adult = r_adult;
	}
	public int getR_child() {
		return r_child;
	}
	public void setR_child(int r_child) {
		this.r_child = r_child;
	}
	public int getR_count() {
		return r_count;
	}
	public void setR_count(int r_count) {
		this.r_count = r_count;
	}
	public String getR_fileName() {
		return r_fileName;
	}
	public void setR_fileName(String r_fileName) {
		this.r_fileName = r_fileName;
	}

}
